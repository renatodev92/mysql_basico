-- O comando GRANT é utilizado para conceder: Privilégios de sistema para usuários e papéis; Papéis para usuários e papéis (Tanto os privilégios quanto os papéis são locais, globais ou externos); Privilégios de objeto para um objeto em particular para usuários, papéis e PUBLIC.

-- O comando REVOKE revoga, de um ou mais usuários ou grupos de usuários, privilégios concedidos anteriormente.
